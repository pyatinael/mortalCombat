#pragma once

#include <SFML/Graphics.hpp>
#include <vector>


/**
 * @brief Класс для хранения и проигрывания покадровой анимации.
 *
 * Animation содержит набор текстур-кадров, время показа одного кадра
 * и текущий индекс кадра. Класс используется вместе с Animator для смены
 * кадров у спрайта персонажа.
 */
class Animation {
private:
    /**
    * @brief Список указателей на текстуры, из которых состоит анимация.
    *
    * Каждый элемент вектора соответствует одному кадру анимации.
    * Указатели используются для того, чтобы не копировать текстуры.
    */
    std::vector<sf::Texture*> frames;
    /**
     * @brief Время показа одного кадра анимации в секундах.
     */
float frameTime = 0.1f;

    /**
     * @brief Таймер, который считает время с момента последней смены кадра.
     */
    float timer = 0.f;

    /**
     * @brief Индекс текущего отображаемого кадра.
     */
    int currentFrame = 0;

public:
    /**
     * @brief Создаёт пустую анимацию с параметрами по умолчанию.
     */
    Animation() = default;

    /**
     * @brief Создаёт анимацию с заданным временем показа одного кадра.
     *
     * @param frameTime Время показа одного кадра в секундах.
     */
    Animation(float frameTime);

    /**
     * @brief Добавляет новый кадр в анимацию.
     *
     * @param texture Текстура, которая будет использоваться как кадр анимации.
     */
    void addFrame(sf::Texture& texture);

    /**
     * @brief Обновляет текущий кадр анимации.
     *
     * Увеличивает внутренний таймер и, если прошло достаточно времени,
     * переключает анимацию на следующий кадр.
     *
     * @param dt Время, прошедшее с предыдущего обновления, в секундах.
     */
    void update(float dt);

    /**
     * @brief Применяет текущий кадр анимации к спрайту.
     *
     * Устанавливает текстуру текущего кадра для переданного спрайта.
     *
     * @param sprite Спрайт, к которому применяется текущий кадр анимации.
     */
    void applyToSprite(sf::Sprite& sprite);

    /**
     * @brief Сбрасывает анимацию к первому кадру.
     *
     * Обнуляет таймер и устанавливает индекс текущего кадра в начало.
     */
    void reset();
};


